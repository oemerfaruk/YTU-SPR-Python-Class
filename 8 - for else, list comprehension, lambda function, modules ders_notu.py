"""#Lambda Function

-Lambda fonksiyonları, özellikle küçük ve basit işlemler için hızlı ve kompakt çözümler sunmak amacıyla kullanılır.

**Syntax:**

lambda *arguments* : *expression*
"""

x = lambda a : a + 5
x(4)

multiply = lambda a, b : a * b
