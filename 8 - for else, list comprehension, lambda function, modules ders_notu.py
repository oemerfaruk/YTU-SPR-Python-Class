# Lambda fonksiyonunu başka bir fonksiyonun içinde kullanmak için

def myfunc(n):
  return lambda a : a * n

mydoubler = myfunc(2) # Girdiğin her sayıyı 2 ile çarpan fonksiyon
mytripler = myfunc(3) # Girdiğin her sayıyı 3 ile çarpan fonksiyon

print(mydoubler(24))
print(mytripler(24))

